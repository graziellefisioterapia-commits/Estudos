L = [5,7,2,9,4,1,3]


print (len(L)) #"len" descreve o tamanho da lista
print (min(L)) #"min" menor valor da lista
print (max(L)) #"max" maior valor da lista
print (sum(L)) #"sun" soma de todos os elementos da lista
print (sorted(L)) #"sorted" organiza do menor para o maior (crescente)
print (sorted(L,reverse = True)) # "sorted(numero,reverse = True" forma de organizar em ordem decrescente